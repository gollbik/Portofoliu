import "@/app/globals.css";
import NavBar from "@/components/NavBar";
import type { GetServerSidePropsContext, Metadata } from "next";
import { Roboto } from "next/font/google";
import { cookies } from "next/headers";

const roboto = Roboto({ preload: true, weight: "400", subsets: ["latin"] });

export const metadata: Metadata = {
  title: "RAWR",
  description: "Generated by create next app",
};

export async function getServerSideProps({ req }: GetServerSidePropsContext) {
  const theme = req.cookies["theme"] || "night";

  return { props: { theme } };
}

export default async function RootLayout({
  children,
}: {
  children: React.ReactNode;
  theme: any;
}) {
  const theme = cookies().get("theme");

  return (
    <html lang="en" data-theme={theme?.value}>
      <body className={`${roboto.className} px-[20.5rem] pt-8`}>
        <NavBar theme={theme?.value} />
        {children}
      </body>
    </html>
  );
}
